#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		len;
	int		i;

	if (!src)
		return (NULL);
	len = ft_strlen(src);
	copy = malloc(len * sizeof(char) + 1);
	if (!copy)
		return (NULL);
	i = 0;
	while (i < len)
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
